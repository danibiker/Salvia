"    }                                                                  \n"
"                                                                       \n"
"    Out.Pos.x = In.Pos.x * scale.x;                                    \n"
"    Out.Pos.y = In.Pos.y * scale.y;                                    \n"
"    Out.Pos.z = 0.0f;                                                  \n"
"    Out.Pos.w = 1.0f;                                                  \n"
"                                                                       \n"
"    Out.UV = In.UV;                                                    \n"
"    return Out;                                                        \n"
"}																		\n";	

